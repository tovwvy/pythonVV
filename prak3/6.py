# Зчитування цілих чисел a та b з клавіатури
a = int(input("Введіть число a: "))
b = int(input("Введіть число b: "))

# Перевірка чисел на рівність
if a == b:
  # Якщо числа рівні, замінюємо їх нулями
  a = 0
  b = 0
else:
  # Якщо числа не рівні, знаходимо більше число
  max_number = max(a, b)
  # Замінюємо a та b на max_number
  a = max_number
  b = max_number

# Виводимо результати
print("a =", a)
print("b =", b)

