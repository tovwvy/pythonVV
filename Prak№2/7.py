# Присвоєння значень змінним
var_a = 10
var_b = 5

# Перевірка значень змінних
if var_a > var_b:
    # Розрахунок різниці
    var_c = var_a - var_b
    # Виведення результату
    print("Різниця значень:", var_c)
elif var_a < var_b:
    # Розрахунок суми
    var_c = var_a + var_b
    # Виведення результату
    print("Сума значень:", var_c)
else:
    # Присвоєння значення var_a
    var_c = var_a
    # Виведення результату
    print("Значення третьої змінної:", var_c)
